class RandomGraph {
public:
	double probability(int, int);
};


double RandomGraph::probability(int n, int p) {
	double ans=0;
	if(n<4 or p==0)
		return ans;
	else
	{
		long int nc;
		nc=(n*(n-1)*(n-2)*(n-3))/24;
		double npr,pr;
		pr=double(p)/1000;
		npr=1-pr;
		double imp;
		imp=((npr*npr*npr*npr*npr*npr)+(6*pr*npr*npr*npr*npr*npr)+(15*pr*pr*npr*npr*npr*npr)+(4*pr*pr*pr*npr*npr*npr));
		ans=imp;
		for(int i=0;i<nc;i++)
		{
			ans=ans*ans;
			imp=imp*imp;
		}
		ans=1-ans;
		return imp;
	}
}


