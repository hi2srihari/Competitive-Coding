class LongWordsDiv2 {
public:
	string find(string);
};

string LongWordsDiv2::find(string word) {
	for(int i=0;i<word.size();i++)
	{
		if(int(word[i])>90 or int(word[i])<65)
			return "Dislikes";
		if(i>0)
		{
			if(word[i]==word[i-1])
				return "Dislikes";
		}
	}
	for(int i=0;i<word.size();i++)
	{
		for(int j=i+1;(j>i && j<word.size());j++)
		{
			for(int k=j+1;k<word.size();k++)
			{
				for(int l=k+1;l<word.size();l++)
				{
					if(word[i]==word[k] and word[j]==word[l])
						return "Dislikes";
				}
			}
		}
	}
	return "Likes";
}


