class FibonacciDiv2 {
public:
	int find(int);
};

int FibonacciDiv2::find(int N) {
	long int x1,x2,xtemp;
	x1=0;
	x2=1;
	for(;x2<N;)
	{
		xtemp=x1;
		x1=x2;
		x2=x1+xtemp;
	}
	if((x2-N)>(N-x1))
		return (N-x1);
	else
		return (x2-N);
}


